b = 5
print("Suma", a + b)
print("Resta", a - b)
print("Multiplicación", a * b)
print("División", a / b)
print("División entera", a // b)
print("Módulo o resto", a % b)
print("Potencia", a**b)
print("Raíz cuadrada de 81 es", 81 ** (1 / 2))

